from pathlib import Path

# ============================================================================
# 第一部分：路径定义
# ============================================================================
# ROOT 是整个项目的根目录（sustech-rag/）
# 使用 pathlib.Path 而非字符串，因为跨平台兼容性更好（Windows/Mac/Linux 都能用）
ROOT = Path(__file__).parent
DATA_DIR = ROOT / "data"          # 数据目录：存放原始数据、清洗后数据、chunk 等
RAW_DIR = DATA_DIR / "raw"        # 原始爬虫数据（HTML JSONL）
PROC_DIR = DATA_DIR / "processed" # 清洗后的结构化文档
CHUNK_DIR = DATA_DIR / "chunks"   # 切分好的 chunk 文件
INDEX_DIR = ROOT / "index_store"  # ChromaDB 向量索引 + BM25 索引
RESULTS_DIR = ROOT / "results"    # 实验结果输出（评分、延迟统计等）

# ============================================================================
# 第二部分：文档分块（Chunking）参数
# ============================================================================
# 为什么需要多种 chunk 大小？
# → 消融实验需要对比不同 chunk 大小对检索效果的影响。
#   - 太小（300字）：语义碎片化，丢失上下文
#   - 太大（900字）：信息稀释，检索精度下降
#   - 默认（600字）：论文中常用的平衡点，兼顾语义完整性和检索精度

CHUNK_SIZE_DEFAULT = 600  # 字符数（不是 token 数！中文字符 ≈ 1 token）
CHUNK_SIZE_SMALL = 300    # 小 chunk，用于对比实验 A1
CHUNK_SIZE_LARGE = 900    # 大 chunk，用于对比实验 A2
CHUNK_OVERLAP = 100       # chunk 之间的重叠量，保证跨 chunk 的上下文不被切断
MIN_CHUNK_LEN = 100       # 低于此长度的 chunk 直接丢弃（多半是导航条/页脚残留）

# ============================================================================
# 第三部分：检索（Retrieval）参数
# ============================================================================
# 检索管线的核心设定：
#   用户问题 → [可选 HyDE 扩展] → 稠密检索 top-50 + BM25 top-50
#   → RRF 融合取 top-20 → Cross-Encoder 重排序 top-5 → 拼接上下文 → LLM 生成

DENSE_TOP_K = 50   # 稠密检索（bge-m3 向量相似度）召回的数量
BM25_TOP_K = 50    # 稀疏检索（BM25 关键词匹配）召回的数量
# 为什么 top-50 这么大？→ RRF 融合需要足够大的候选池才能发挥优势。
# 太少（比如 top-10）会让两个检索器的重合度过低，RRF 退化为单检索器。

RRF_K = 60         # RRF（Reciprocal Rank Fusion）平滑常数
# 为什么 k=60？
# → Cormack et al. (2009) 原始论文推荐值。k 越大，融合越"平滑"，
#   极端 rank 差异的影响越小。60 是 TREC 实验验证过的最优默认值。

RRF_FUSION_TOP = 20  # RRF 融合后保留的候选数，作为 Reranker 的输入
RERANK_TOP_K = 5     # Cross-Encoder 重排序后保留的最终 chunk 数
# 为什么重排序后只要 5 个？
# → LLM 的上下文窗口有限（7B 模型 ~8K tokens），5 个 chunk ≈ 3000-4000 token，
#   加上 system prompt 和用户问题，刚好在安全范围内。太长的上下文反而稀释信号。

# ============================================================================
# 第四部分：模型选择
# ============================================================================
# 我们的模型选型与队友的差异（学术区分度）：
# - 队友：Qwen3 系列（Embed-4B / Reranker-4B / 27B-vLLM）
# - 我们：bge-m3 系列（MTEB 中文榜单 leader）+ Qwen2.5-7B（更轻量，本地可跑）

# Embedding 模型：将文本转为向量表示
EMBED_MODEL = "BAAI/bge-m3"
# 为什么选 bge-m3？
# 1. MTEB 中文检索榜单长期 leader
# 2. 支持多语言（中英混合），非常适合南科大双语校园场景
# 3. 模型大小适中（~2.2GB），RTX 6000 Blackwell 上 batch encoding 很快
# 4. 支持 instruction prefix（query 端可以加指令前缀提升效果）

# Reranker 模型：对候选 chunk 做精细的相关性打分
RERANK_MODEL = "BAAI/bge-reranker-v2-m3"
# Cross-Encoder 的工作方式：不是单独 encode query 和 document，
# 而是把 [query, document] 拼接后一起过模型，输出相关性分数。
# 比 bi-encoder（双塔）更精确，但更慢 → 所以我们只在 top-20 上用。

# 本地 LLM：通过 Ollama 在本地运行的对话模型
LOCAL_LLM = "qwen2.5:7b"  # Ollama model name

# 远程 API LLM：DeepSeek API（OpenAI 兼容，中文能力强，性价比高）
API_LLM = "deepseek-chat"  # DeepSeek-V4 模型
DEEPSEEK_BASE = "https://api.deepseek.com/v1"  # DeepSeek 的 OpenAI 兼容端点

# ============================================================================
# 第五部分：来源权威性权重
# ============================================================================
# 我们的创新点之一：Source Authority Scoring
# 不同来源的可信度不同。学校官网的公告 > 某篇新闻稿。
# 这些权重在 Reranker 分数之后叠加，作为最终的排序信号。

SOURCE_AUTHORITY = {
    "official": 1.0,
    "admission": 0.95,
    "library": 0.90,
    "department": 0.85,
    "manual": 0.80,
    "news": 0.75,
    "unknown": 0.50,
}

# ============================================================================
# 第六部分：置信度与拒绝回答（Abstention）阈值
# ============================================================================
# 我们的创新点之一：Evidence-Based Confidence Abstention
# 当检索到的证据太弱时，系统应该"拒绝回答"而不是"瞎编"。
# 这在校园场景中很重要——错误的考试日期、错误的办公时间会造成实际困扰。
# 注意：此阈值需要在实验阶段根据实际 RRF 分数分布进行校准。

ABSTENTION_THRESHOLD = 0.008
# RRF 分数范围约 [0, 0.033]（rank=1 在两检索器: 1/61+1/61≈0.033）。
# rank=50 在一个检索器: 1/110≈0.009。阈值 0.008 意味：
#   top-50 内的文档 → 不拒答；两个检索器都不在 top-50 → 拒答

